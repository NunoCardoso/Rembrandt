<H2>Dependências</H2>

<H3>versões 1.0 ou superiores do REMBRANDT</H3>

<UL>	
<LI><P><a href="http://groovy.codehaus.org">Groovy</A>, uma linguagem de programação 100% Java. Versão recomendada: Groovy 1.7.</P></LI>
<LI><P><A href="http://code.google.com/p/digmap/wiki/LuceneGeoTemporal">LGTE</A>, um motor de busca de documentos desenvolvido por Jorge Machado.
</P></LI>
</UL>	
<LI><P><a href="http://logging.apache.org/log4j">Log4J</A>, gestor de logs. Versão recomendada: log4j 1.2.12 ou superior.</P></LI>
<LI><P><a href="http://lucene.apache.org/">Lucene</A>, indexação e recuperação de texto. Versão recomendada: Lucene  2.4 ou superior.</P></LI>
	
<LI><P><a href="http://www.mysql.com/products/connector-j">MySQL Connector/J</A>, para a comunicação Java com o MySQL. Versão recomendada: MySQL Connector 5.1.5 ou superior.</P></LI>

<LI><P><a href="http://xstream.codehaus.org">XStream</A>, serialização de objectos. Versão recomendada: XStream 1.3 ou superior.</P></LI>
<LI><P><a href="http://jena.sourceforge.net/downloads.html">Jena</A>, uma <I>framework</I> para a Semantic Web. Os pacotes Jana, SDB e ARQ podem ser obtidos em <a href="http://jena.sourceforge.net/downloads.html">http://jena.sourceforge.net/downloads.html</a>.</P></LI>
<LI><P><A HREF="http://commons.apache.org/logging">Apache Commons Logging</A>, para gestão de parâmetros de linha de comandos. Versão recomendada: commons-logging-1.1.1.jar.</P></LI>
<LI><P><A HREF="http://icu-project.org/download/3.4.html">ICU</A>, pacote IBM de suporte a Unicode que é uma dependência de outro pacote acima citado. Versão recomendada: 3.4.</P></LI>
<LI><P><A HREF="http://sourceforge.net/projects/json-lib/">JSON lib</A>, um conversor JSON. Versão recomendada: json-lib-2.3-jdk1.5.</P></LI>		 
<LI><P><A HREF="http://www.restlet.org/downloads/">org.restlet</A>, uma implementação RESTful cliente/servidor. Ver~são recomendada: 2.0.</P></LI>
<LI><P><A HREF="http://xml.apache.org/xalan-j/downloads.html">XercesImpl</A>, o parser XML da Apache, e o <A HREF="http://www.extreme.indiana.edu/xgws/xsoap/xpp/">XPP3</A>, outro parser XML (versão recomendada: xpp3_min-1.1.3.4.O.jar)</P></LI>	

<H3>Versões anteriores à 1.0 do REMBRANDT</H3>

<UL>
<LI><P><A HREF="http://groovy.codehaus.org/XMLRPC">Groovy XML-RPC</A>, comunicação cliente-servidor por RPC. Versão recomendada: Groovy XML-RPC 0.4 ou superior.</P></LI>
</UL>

<P>Se quiser usar o REMBRANDT em processos Map-Reduce (<a href="http://labs.google.com/papers/mapreduce.html" target="_blank">o que é isso?</a>), precisa do <B>Hadoop</B>, que pode ser obtido em <a href="http://hadoop.apache.org">http://hadoop.apache.org</A>. Versão recomendada: Hadoop 0.18 ou superior. Note que este pacote não é necessário para usar o REMBRANDT de forma normal é só para utilizadores mais intensivos que queiram etiquetar colecções de texto de grandes dimensões. Sim, há pessoas neste mundo que querem fazer isso. </P>

